# ----------------------------------------------------------------------------

if (sys.nframe() == 0) {
    # Script is being executed from the command line

    # ---------------- [Validate arguments] -------------

    xargs <- parser$parse_args()



    if (is.null(xargs$pileup) && (is.null(xargs$snp_tumour) || is.null(xargs$snp_normal))) {
        write("Please provide a pileup file or both a tumour and a normal bam file", stderr())
        quit(status = 1)
    }
    if (!is.null(xargs$snp_tumour) && xargs$snp_tumour == xargs$snp_normal) {
        write("Input bam files for tumour and normal cannot be the same", stderr())
        quit(status = 1)
    }

    if (is.null(xargs$pileup) && is.null(xargs$snp_vcf)) {
        write("Please use the --snp_vcf option to provide a VCF file of SNPs", stderr())
        quit(status = 1)
    }
    if (xargs$depth[1] > xargs$depth[2]) {
        write("Error in argument --depth: min > max", stderr())
        quit(status = 1)
    }
    if (xargs$cval[1] > xargs$cval[2]) {
        write("Error in argument --cval. Value for pre-processing (first argument)\nmust be lower than the value for processing (second argument)", stderr())
        quit(status = 1)
    }
    if (!is.null(xargs$dipLogR) & !is.null(xargs$purity_cval)) {
        write("Error: only one between --dipLogR and --purity-cval may be specified", stderr())
        quit(status = 1)
    }


    # # # test values
    # xargs=list(
    #     # snp_tumour="/lab/ni/mavrommk/workspace/cnv_facets/test-kostas/PersonalisPS104976950_DNA-WES.bam",
    #     # snp_normal="/lab/ni/mavrommk/workspace/cnv_facets/test-kostas/PersonalisPSDB9536713-WES.bam",
    #     pileup="/lab/ni/mavrommk/workspace/cnv_facets/test-kostas/test-failed.csv.gz",
    #     snp_vcf="/lab/ni/mavrommk/workspace/cnv_facets/test-kostas/gnomad.exomes.afonly.v4.1.vcf.gz",
    #     snp_mapq=5,
    #     snp_baq=10,
    #     snp_nprocs=8,
    #     depth=c(25, 4000),
    #     targets="/lab/ni/mavrommk/workspace/cnv_facets/test-kostas/ImmunoID_NeXT_target_hg38_liftover.bed",
    #     cval=c(25, 150),
    #     nbhd_snp="auto",
    #     gbuild="hg38",
    #     unmathed=FALSE,
    #     focal=TRUE,
    #     diplogR=NULL,
    #     out="/lab/ni/mavrommk/workspace/cnv_facets/test-kostas/test-failed",
    #     rnd_seed="The name of the input file",
    #     snp_count_orphans = FALSE,
    #     no_cov_plot = FALSE
    # )
    # def_nbhd= list("auto", 250)



    est_insert_size <- NA
    if (xargs$nbhd_snp == "auto") {
        if (!is.null(xargs$pileup)) {
            nbhd_snp <- def_nbhd[[2]]
        } else {
            nbhd_snp <- avg_insert_size(xargs$snp_normal, default = def_nbhd[[2]])
            est_insert_size <- round(nbhd_snp, 2)
        }
    } else if (is.na(as.integer(xargs$nbhd_snp))) {
        write('Argument to --nbhd-snp must be "auto" or an integer', stderr())
        quit(status = 1)
    } else {
        message("default is ", xargs$nbhd_snp)
        nbhd_snp <- as.integer(xargs$nbhd_snp)
    }
    nbhd_snp <- as.integer(nbhd_snp)

    write(sprintf("[%s] NBHD for SNP is set to  %d...", Sys.time(), nbhd_snp), stderr())

    if (xargs$focal) {
        write(sprintf("[%s] Focal search is enabled...", Sys.time()), stderr())
    }

    # --------------- [Start processing] ----------------

    dir.create(dirname(xargs$out), recursive = TRUE, showWarnings = FALSE)

    if (xargs$rnd_seed == def_rnd) {
        if (!is.null(xargs$pileup)) {
            seed <- sum(utf8ToInt(xargs$pileup))
        } else {
            seed <- sum(utf8ToInt(paste(xargs$snp_normal, xargs$snp_tumour)))
        }
    } else if (!is.na(suppressWarnings(as.numeric(xargs$rnd_seed)))) {
        seed <- as.numeric(xargs$rnd_seed)
    } else {
        seed <- sum(utf8ToInt(xargs$rnd_seed))
    }
    write(sprintf("[%s] Seed is set to  %d...", Sys.time(), seed), stderr())
    set.seed(seed)

    if (is.null(xargs$pileup)) {
        write(sprintf("[%s] Generating pileup from the BAM files t:%s, n:%s...", Sys.time(), xargs$snp_tumour, xargs$snp_normal), stderr())
        pileup <- paste0(xargs$out, ".csv.gz")
        basename(xargs$snp_tumour)
        exec_snp_pileup_parallel(
            snp_vcf = xargs$snp_vcf,
            output = pileup,
            normal_bam = xargs$snp_normal,
            tumour_bam = xargs$snp_tumour,
            mapq = xargs$snp_mapq,
            baq = xargs$snp_baq,
            pseudo_snp = nbhd_snp,
            nprocs = min(8, ceiling(xargs$snp_nprocs / 2)),
            keep_orphans = xargs$snp_count_orphans
        )
    } else {
        pileup <- xargs$pileup
        write(sprintf("[%s] Loading from existing pileup  %s...", Sys.time(), pileup), stderr())
    }

    write(sprintf("[%s] Loading file %s...", Sys.time(), pileup), stderr())
    rcmat <- readSnpMatrix2(pileup, xargs$gbuild)
    if (nrow(rcmat$pileup) == 0) {
        write(sprintf("[%s] Analysis stopped: pileup file %s has no records", Sys.time(), pileup), stderr())
        quit(status = 1)
    }

    # Read targets file
    if (!is.null(xargs$targets)) {
        targets <- data.table(read.table(xargs$targets, comment.char = "#", header = FALSE, sep = "\t"))
        write(sprintf("[%s] Reading targets from file %s", Sys.time(), xargs$targets), stderr())
        if (rcmat[["chr_prefix"]]) {
            # Original input has chr prefix, so we need to strip it from targets DF
            n <- length(unique(targets$V1))
            targets[, V1 := sub("^chr", "", V1)]
            stopifnot(n == length(unique(targets$V1)))
        }
    } else {
        targets <- NULL
    }
    rcmat_flt <- filter_rcmat(
        rcmat = rcmat[["pileup"]], min_ndepth = xargs$depth[1],
        max_ndepth = xargs$depth[2], target_bed = targets
    )

    if (xargs$no_cov_plot == FALSE) {
        write(sprintf("[%s] Plotting histogram of coverage...", Sys.time()), stderr())
        plot_coverage(
            rcmat = rcmat[["pileup"]], rcmat_flt = rcmat_flt,
            fname = paste0(xargs$out, ".cov.pdf"), title = paste0("Depth of coverage\n", xargs$out)
        )
        write(sprintf("[%s] Plotting histogram of coverage in file %s", Sys.time(), paste0(xargs$out, ".cov.pdf")), stderr())
    }

    rcmat[["pileup"]] <- NULL

    x_ <- gc(verbose = FALSE)

    # ------------- [Run FACETS] --------------

    # pre_cvals=if(xargs$cval[1]==-1){
    #     seq( 25, 100, 25)
    # }else{
    #     c(xargs$cval[1] )
    # }
    pre_cval <- xargs$cval[1]
    proc_cvals <- c(xargs$cval[2])
    write(sprintf("[%s] preCVAL=%d, procCVAL=%d, nbhd_snp=%d(%s)", Sys.time(), xargs$cval[1], xargs$cval[2], nbhd_snp, xargs$nbhd_snp), stderr())
    orig_nbhd_snp <- nbhd_snp
    if (xargs$focal) {
        nbhd_snp <- unique(c(nbhd_snp, 30, 45, 60, seq(80, ceiling(nbhd_snp / 40) * 40, 40)))
        proc_cvals <- unique(c(xargs$cval[2], 30, 40, seq(50, max(250, ceiling(xargs$cval[2] / 50) * 50), 50)))
    }
    grid <- expand.grid(proc_cval = proc_cvals, nbhd_snp = nbhd_snp, unmatched = xargs$unmatched)
    if (!is.null(xargs$purity_cval)) {
        write(sprintf("[%s] Preliminary purity estimation with warm up value %d...", Sys.time(), xargs$purity_cval), stderr())
        facets <- run_facets(
            pre_rcmat = rcmat_flt,
            pre_gbuild = xargs$gbuild,
            pre_snp.nbhd = nbhd_snp,
            pre_het.thresh = ifelse(xargs$unmatched, 0.1, 0.25),
            pre_cval = pre_cval,
            pre_deltaCN = 0,
            pre_unmatched = xargs$unmatched,
            pre_ndepth = 1, # We subset the input matrix instead of using ndepth*
            pre_ndepthmax = 1e8, # options
            proc_cval = xargs$purity_cval,
            proc_min.nhet = 15,
            proc_dipLogR = NULL,
            emcncf_unif = FALSE,
            emcncf_min.nhet = 15,
            emcncf_maxiter = 20,
            emcncf_eps = 1e-3
        )
        proc_dipLogR <- facets$emcncf_fit$dipLogR
        write(sprintf("[%s] logR ploidy estimation used for final processing step %f.", Sys.time(), proc_dipLogR), stderr())
    } else {
        proc_dipLogR <- xargs$dipLogR
        write(sprintf("[%s] logR ploidy estimation used for final processing step NULL.", Sys.time()), stderr())
    }




    # get the memory size for the objects we want to work with
    memory_object <- 2.5 * (object.size(rcmat_flt) %>% as.numeric()) + 1024^3
    memory_available <- memuse::Sys.meminfo()$freeram %>% as.numeric()
    write(sprintf("[%s] The memory requirement is  %d bytes", Sys.time(), memory_object), stderr())
    options(future.globals.maxSize = memory_object)
    use_cores <- min(
        xargs$snp_nprocs,
        floor(memory_available / memory_object)
    )
    write(sprintf("[%s] Running solutions using %d cores.", Sys.time(), use_cores), stderr())
    oplan <- future::plan(future::multisession(workers = use_cores))


    write(sprintf("[%s] Starting parallel processing...", Sys.time()), stderr())

    internal_results <- future.apply::future_mapply(
        function(proc_cval, nbhd_snp, unmatched) {
            write(sprintf("[%s] Solution parameters proc_cval: %d, nbhd: %d, unmatched mode: %s", Sys.time(), proc_cval, nbhd_snp, as.character(unmatched)), stderr())

            facets <- run_facets(
                pre_rcmat = rcmat_flt,
                pre_gbuild = xargs$gbuild,
                pre_snp.nbhd = nbhd_snp,
                pre_het.thresh = ifelse(unmatched, 0.1, 0.25),
                pre_cval = pre_cval,
                pre_deltaCN = 0,
                pre_unmatched = unmatched,
                pre_ndepth = 1, # We subset the input matrix instead of using ndepth*
                pre_ndepthmax = 1e8, # options
                proc_cval = proc_cval,
                proc_min.nhet = 15,
                proc_dipLogR = proc_dipLogR,
                emcncf_unif = FALSE,
                emcncf_min.nhet = 15,
                emcncf_maxiter = 20,
                emcncf_eps = 1e-3
            )
            # -----------------------------------------

            write(sprintf("[%s] Writing output", Sys.time()), stderr())
            cncf <- copy(facets$emcncf_fit$cncf)

            reset_chroms(cncf = cncf, gbuild = xargs$gbuild, chr_prefix = rcmat$chr_prefix)
            classify_cnv(cncf)

            if (is.null(xargs$annotation) == FALSE) {
                annotate(cncf, xargs$annotation)
            }
            outpattern <- paste0(xargs$out, "_", pre_cval, "_", proc_cval, "_", nbhd_snp, "_", ifelse(unmatched == TRUE, "unmatchednormal", "matchednormal"))
            main.solution <- FALSE
            if (nbhd_snp == orig_nbhd_snp & pre_cval == xargs$cval[1] & proc_cval == xargs$cval[2]) {
                main.solution <- TRUE
                outpattern <- paste0(xargs$out)
            }
            if (!is.null(xargs$dipLogR)) {
                outpattern <- paste0(outpattern, "_", xargs$dipLogR)
            }
            if (main.solution == FALSE & xargs$focal) {
                dir.create(file.path(dirname(outpattern), "focal"), showWarnings = FALSE)
                outpattern <- file.path(dirname(outpattern), "focal", basename(outpattern))
            }
            vcf_tmp <- tempfile(pattern = paste0(basename(outpattern), "."), tmpdir = dirname(outpattern), fileext = ".vcf")
            cmd <- sprintf("##%sCommand=%s; Version=%s; Date=%s", getScriptName(), paste(commandArgs(), collapse = " "), VERSION, Sys.time())

            extra <- c(
                mainsolution = main.solution,
                purity = ifelse(is.na(facets$emcncf_fit$purity), 0.1, facets$emcncf_fit$purity),
                ploidy = facets$emcncf_fit$ploidy,
                dipLogR = facets$emcncf_fit$dipLogR,
                emflags = facets$emcncf_fit$emflags,
                estinsertsize = est_insert_size,
                prepuritycval = ifelse(is.null(xargs$purity_cval), "No preliminary cval", xargs$purity_cval),
                precval = pre_cval,
                proccval = proc_cval,
                snpnbhd = nbhd_snp,
                usersnpnbhd = xargs$nbhd_snp,
                matchednormal = !unmatched,
                tumorsample = ifelse(is.null(xargs$snp_tumour), basename(xargs$pileup), gsub(".bam$", "", basename(xargs$snp_tumour))),
                normalsample = ifelse(is.null(xargs$snp_normal), basename(xargs$pileup), gsub(".bam$", "", basename(xargs$snp_normal)))
            )

            header <- make_header(
                gbuild = xargs$gbuild,
                genomes = genomes,
                is_chrom_prefixed = rcmat$chr_prefix,
                cmd = cmd,
                extra = extra
            )
            write(header, vcf_tmp)

            for (i in 1:nrow(cncf)) {
                record <- paste(facetsRecordToVcf(cncf[i]), collapse = "\t")
                if (!is.null(record) & nchar(record) > 0) {
                    write(record, vcf_tmp, append = TRUE)
                } else {
                    write(sprintf("[%s] Warning: empty record for segment %d. Check facets.err.log", Sys.time(), i), stderr())
                    write.table(as.data.frame(cncf)[i - 10:i + 10, ], paste0(xargs$out, ".facets.err.log"), row.names = FALSE, col.names = TRUE, quote = FALSE)
                }
            }

            tryCatch(
                {
                    x_ <- bgzip(vcf_tmp, dest = paste0(outpattern, ".vcf.gz"), overwrite = TRUE)
                    x_ <- indexTabix(paste0(outpattern, ".vcf.gz"), format = "vcf4")
                    unlink(vcf_tmp)
                },
                error = function(e) {
                    write(sprintf("[%s] Error in bgzip: %s", Sys.time(), e), stderr())
                    unlink(vcf_tmp)
                }
            )

            write(sprintf("[%s] Plotting genome...", Sys.time()), stderr())
            png(paste0(outpattern, ".cnv.png"), units = "px", width = 1600, height = 1600, res = 300)
            sname <- sprintf("%s; ploidy= %.2f; purity= %.2f", basename(outpattern), facets$emcncf_fit$ploidy, facets$emcncf_fit$purity)
            plotSample(x = facets$proc_out, emfit = facets$emcncf_fit, sname = sname)
            x_ <- dev.off()

            write(sprintf("[%s] Plotting spider...", Sys.time()), stderr())
            pdf(paste0(outpattern, ".spider.pdf"), width = 16 / 2.54, height = 14 / 2.54)
            par(las = 1, mar = c(3, 3, 1, 1), mgp = c(1.5, 0.5, 0), tcl = -0.3)
            logRlogORspider(facets$proc_out$out, facets$proc_out$dipLogR)
            x_ <- dev.off()

            res <- list(
                main_solution = extra[["mainsolution"]],
                purity_cval = extra[["prepuritycval"]],
                pre_cval = extra[["precval"]],
                proc_cval = extra[["proccval"]],
                SNP_density = extra[["snpnbhd"]],
                dipLogR = extra[["dipLogR"]],
                estimated_insert_size = extra[["estinsertsize"]],
                user_pre_cval = xargs$cval[1],
                user_proc_cval = xargs$cval[2],
                user_SNP_density = extra[["usersnpnbhd"]],
                ploidy = facets$emcncf_fit$ploidy,
                purity = facets$emcncf_fit$purity,
                n_segments = nrow(facets$emcncf_fit$cncf),
                matched_normal = !unmatched,
                emflags = facets$emcncf_fit$emflags
            )

            facets <- NULL
            gc(verbose = FALSE)
            # write(sprintf("[%s] Solution parameters proc_cval: %d, nbhd: %d, unmatched mode: %s. Completed!!", Sys.time(), proc_cval, nbhd_snp, as.character(unmatched)), stderr())
            # write(sprintf("[%s] Solution stored in: %s", Sys.time(), outpattern), stderr())
            return(res)
        }, grid$proc_cval,
        grid$nbhd_snp,
        grid$unmatched,
        future.seed = TRUE,
        future.globals = TRUE,
        SIMPLIFY = FALSE
    ) %>% bind_rows()

    # --- end of proc_cval loop
    future::plan(oplan)
    # produce a table with some statistics
    options(width = 10000)
    internal_results %>% print.data.frame(., digits = 3, quote = FALSE, right = TRUE, row.names = FALSE, max = 1000)
    write.table(internal_results, paste0(xargs$out, ".facets.log"), row.names = FALSE, col.names = TRUE, quote = FALSE)
    si <- capture.output(sessionInfo())
    write(si, stderr())
}
